
l1 = [10,20,30,40,50,60,70,10,5,30,50]
print(l1) # [10,20,30,40,50,60,70,10,5,30,50]
print(type(l1)) # <class 'list'>
print(len(l1))  # 11

print("==========================")

s1 = set(l1) # list is converting into set
print(s1) # {20,10,5,40,30,50,60,70}
print(type(s1)) # <class 'set'>
print(len(s1)) # 8
